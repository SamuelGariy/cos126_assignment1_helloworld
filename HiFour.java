public class HiFour {
    public static void main(String[] args) {

        // takes input arguments
        String name1 = args[0];
        String name2 = args[1];
        String name3 = args[2];
        String name4 = args[3];

        // prints names in reverse order
        System.out.print("Hi " + name4 + ", " + name3 + ", " + name2);
        System.out.println(", and " + name1 + ".");
    }
}
